    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        double a = Double.parseDouble(txtA.getText());
        double b = Double.parseDouble(txtB.getText());
        double c = Double.parseDouble(txtC.getText());
        double d = Math.pow(b ,2)-4*a*c;
        
        if (d > 0)
        {
            double x1 = (-b+Math.sqrt(d))/2*a;
            double x2 = (-b-Math.sqrt(d))/2*a;
            txtx1.setText(String.format("%.3f", x1).replace(".",","));
            txtx2.setText(String.format("%.3f", x2).replace(".",","));
            
        }
        else 
        {
            double aux = -b/(2*a);
            double x = Math.sqrt(-d)/(2*a);
            String s = String.format("%.2f", aux).replace(".", ",");
            String s1 = String.format("%.2f", aux).replace(".", ",");
            txtx1.setText(s + " + " + s1 + "i");
            txtx2.setText(s + " - " + s1 + "i");
        }
        
        
    }//GEN-LAST:event_btnCalcularActionPerformed

