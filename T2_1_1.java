    private double saldo;
    private void ingreso()
    {
        String Ingreso = JOptionPane.showInputDialog("Ingresa el saldo");
        saldo = Double.parseDouble(Ingreso);
        txtSaldo.setText(String.format("%.2f", saldo));
    }
    private void retiro()
    {
        double retiros = 0;
        do{
            String retira = JOptionPane.showInputDialog("Ingresa el monto que desea retirar");
            retiros = Double.parseDouble(retira);
        }while(saldo<=retiros);
        
        JOptionPane.showMessageDialog(null, "El retiro se realizÃ³ corractamente");
        txtMonto.setText(String.format("%.2f",retiros));
        txtSaldo.setText(String.format("%.2f", (saldo - retiros)));
        
      
    }
