    private void btnCalcularDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularDActionPerformed
        // TODO add your handling code here:
        int x1 = Integer.parseInt(txtIngresoD.getText());
        int x2 = Integer.parseInt(txtIngresoD2.getText());
        int division = x1/x2;
        for (int i = x1; i>=1; i-=x2)
        {
            txtSerieD.setText(txtSerieD.getText()+ i);
            if(i>2)
            {
                txtSerieD.setText(txtSerieD.getText()+" - ");
            }
        }
        txtSerieD.setText(txtSerieD.getText()+ " = " + division);
    }//GEN-LAST:event_btnCalcularDActionPerformed

