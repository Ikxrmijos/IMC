    private void btnCalcularFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularFActionPerformed
        // TODO add your handling code here:
        int fac = Integer.parseInt(txtIngresoF.getText());
        long factorial = 1;
        for (int i = 1; i <= fac; i++)
        {
            factorial *= i;
            
        }
        int j = fac;
        for (int i=1; i<=fac;i++)
        {
            j--;
            txtSerieF.setText(txtSerieF.getText() + (fac-j));
            if (i < fac)
            {
                txtSerieF.setText(txtSerieF.getText()+" * ");
            }
        }
        txtSerieF.setText(txtSerieF.getText() + " = " + factorial);
    }//GEN-LAST:event_btnCalcularFActionPerformed

