    public void calcular ()
    {
        DefaultTableModel modelo = (DefaultTableModel) Tabla.getModel();
        try
        {
            int filas = modelo.getRowCount();
            double subtotal = 0;
            for (int f = 0; f<filas;f++)
            {
                double cant = Double.valueOf(modelo.getValueAt(f, 2).toString());
                double precio = Double.valueOf(modelo.getValueAt(f, 3).toString());
                double importe = cant*precio;
                subtotal += importe;
                modelo.setValueAt(importe, f, 4);
            }
            txtSubtotal.setText(""+subtotal);
            double descuento = 0;
            if(subtotal>1000)
                descuento = subtotal*0.1;
            else if(subtotal>500)
                descuento=subtotal*0.05;
            txtDescuento.setText(""+descuento);
            double iva = (Double.valueOf(txtIVA.getText()) * subtotal)/100;
            double total = subtotal + iva - descuento;
            txtIVAcal.setText(""+iva);
            txtTotal.setText(String.format("%.2f", total)); 
        }catch(Exception ex){}
    }
